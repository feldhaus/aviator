/**
 * Conversion factor for converting radians to degrees.
 */
export const RAD2DEG = 180 / Math.PI;

/**
 * Conversion factor for converting degrees to radians.
 */
export const DEG2RAD = Math.PI / 180;

/**
 * It's half the ratio of the circumference of a circle to its diameter.
 */
export const HALF_PI = Math.PI / 2;

/**
 * It's one quarter the ratio of the circumference of a circle to its diameter.
 */
export const QUARTER_PI = Math.PI / 4;

/**
 * It's twice the ratio of the circumference of a circle to its diameter.
 */
export const TWO_PI = Math.PI * 2;

/**
 * It's twice the ratio of the circumference of a circle to its diameter.
 * TAU is an alias for TWO_PI.
 */
export const TAU = TWO_PI;
